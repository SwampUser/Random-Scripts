#operations binaires

#60 = 0b 0011 1100
#30 = 0b 0001 1110
print(bin(60^30))           # XOR (binaire)
print(60 ^ 30)              # XOR
#   = 0b 0010 0010
#   = 34


#60 = 0b 0011 1100
#30 = 0b 0001 1110
print(bin(60 | 30))         # OR (binaire)
print(60 | 30)              # OR
#   = 0b 0011 1110
#   = 62

#60 = 0b 0011 1100
#30 = 0b 0001 1110
print(bin(60 & 30))         #AND (binaire)
print(60 & 30)              #AND
#   = 0b 0001 1100
#   = 28
